// locale helper for use in app.js, not for templates
function translate(key) {
    var locale = window.locale[lang] || window.locale['en-US'];
    if(key) {
	if(key in locale) {
	    return locale[key];
	}
	else {
	    return '__UNTRANSLATED_STRING__(' + key + ')';
	}
    }
    else {
	return '';
    }
}
